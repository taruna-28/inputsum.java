import java.util.*;

public class inputSum {
    public static void main(String[] args) {

        Scanner tc = new Scanner(System.in);
        System.out.print("Enter a:");
        int a = tc.nextInt();
        System.out.print("Enter b:");
        int b = tc.nextInt();

        int sum = a + b;
        System.out.println("Sum: " + sum);
    }

}
